#While loop

number = 100
while number <= 105:
    print(number)
    number += 1


y = 10
while y >= 5:
    print(y)
    y -= 1



# For loop
for num in range(100, 106):
    print(num)

for num in range(100, 106, 2):
    print(num)

#for loop is also used to print out individual elements in a list
languages = ["Python", "Java", "JavaScript"]
for lang in languages:
    print(lang)

